// # 1,2,3 더하기

// # n : 1~10

// # n=1 -> (1) (1가지)
// # n=2 -> (1+1) (2) (2가지)
// # n=3 -> (1+1+1) (1+2) (2+1) (3) (4가지)
// # n=4 -> (1+1+1+1) (1+1+2) (1+2+1) (2+1+1) (2+2) (1+3) (3+1) (7가지)
// # n=5 -> 1+1+1+1+1 1+1+1+2 1+1+2+1 1+2+1+1 2+1+1+1 1+2+2 2+1+2 2+2+1 1+1+3 1+3+1 3+1+1 2+3 3+2 (13가지)
// # dp[n] = dp[n-1] + dp[n-2] + dp[n-3]
#include <stdio.h>

int main() {
    int t,n;
    int dp[11];
    dp[0]=0; dp[1]=1; dp[2]=2; dp[3]=4;
    for (int i=4; i<11; i++) {dp[i]=dp[i-1]+dp[i-2]+dp[i-3];}
    scanf("%d",&t);
    for (int i=0; i<t; i++) {
        scanf("%d",&n);
        printf("%d\n",dp[n]);
    }
    return 0;
}